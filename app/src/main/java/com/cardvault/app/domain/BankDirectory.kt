    /** 重复出现在多家银行名下的 BIN（应恒为空，由单元测试守护） */
    val duplicateBins: List<String>

    /** 按前缀长度分桶的哈希索引：长度 -> (BIN -> Bank)，查询 O(前缀长度种类数) */
    private val binIndex: Map<Int, Map<String, Bank>>

    init {
        val dupes = mutableListOf<String>()
        val index = mutableMapOf<Int, MutableMap<String, Bank>>()
        for (bank in banks) {
            for (bin in bank.bins) {
                val bucket = index.getOrPut(bin.length) { mutableMapOf() }
                if (bucket.putIfAbsent(bin, bank) != null) dupes += bin
            }
        }
        duplicateBins = dupes
        binIndex = index
    }

    private val binLengthsDesc: List<Int> = binIndex.keys.sortedDescending()

    /** 按“最长前缀优先”查找发卡行；digits 为纯数字卡号（可以是输入中的部分卡号） */
    fun findBank(digits: String): Bank? {
        for (len in binLengthsDesc) {
            if (digits.length < len) continue
            binIndex[len]?.get(digits.take(len))?.let { return it }
        }
        return null
    }

    fun byCode(code: String?): Bank? = banks.firstOrNull { it.code == code }
